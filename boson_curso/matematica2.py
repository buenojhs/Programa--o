import math

x = 8
y = 100

raiz_quadrada = math.sqrt(x)  # Raiz quadrada de x
print(raiz_quadrada)
print(math.ceil(raiz_quadrada))  # Arredondamento para cima
print(math.floor(raiz_quadrada))  # Arredondamento para baixo
print(round(raiz_quadrada, 2))  # Arredondamento com duas casas decimais

logaritmo = math.log10(y)  # Logaritmo natural de y
print(logaritmo)

print(math.pi)  # Valor de pi
print(math.factorial(x))  # Fatorial de x
print(x / math.inf) # Divis√£o por infinito
